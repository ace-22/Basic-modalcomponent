$(document).ready(function(){
$(".abc").hide("");

$(".button").click(function(){
	$(".abc").toggle("")
	
	$(".container").hide("")
	$(".q").click(function(){
		$(".abc").hide("");	
		$(".container").show("");
		})
	$(".qw").click(function(){
		$(".abc").hide("");	
		$(".container").show("");
		})	
	
})
})
